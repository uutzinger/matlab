	-1.29898535321E+01
	1.02380747273E-01
	-4.53334498851E-04
	1.25543825967E-06
	-2.30713052216E-09
	2.92872858087E-12
	-2.63021535739E-15
	1.68646581621E-18
	-7.67810250010E-22
	2.42539021108E-25
	-5.05462095430E-29
	6.24980247408E-33
	-3.47208945317E-37 ];

y = a(1).*ones(size(x)) + a(2).*x + a(3).*x.^2 + a(4).*x.^3 + a(5).*x.^4 + a(6).*x.^5 + a(7).*x.^6 + a(8).*x.^7 + a(9).*x.^8 + a(10).*x.^9 + a(11).*x.^10 + a(12).*x.^11 + a(13).*x.^12 + a(14).*x.^13;


